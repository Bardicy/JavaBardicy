package ReplHomeworks;

public class PatternLoop079 {
	public static void main(String[] args) {
		for (int i=7; i>=2; i--){
		      for (int j =1; j<=i; j++){
		        System.out.print(j + " ");
		      }
		      System.out.println();
		    
		  }
		  for (int r=1; r<=7; r++){
		    for (int c=1; c<=r; c++){
		      System.out.print(c + " ");
		    }
		    System.out.println();
		  }
		  
		
	}

}
